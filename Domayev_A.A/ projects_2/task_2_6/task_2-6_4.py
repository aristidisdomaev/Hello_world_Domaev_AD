print("=== Витязь на распутье ===\n")

print("Перед вами три дороги:")
print("1 — Направо")
print("2 — Налево")
print("3 — Прямо\n")

choice = input("Куда вы поедете? (направо / налево / прямо): ").lower()

if choice == "направо" or choice == "1":
    print("\nТы выбрал путь направо.")
    print("Коня потеряешь, но сам жив останешься.")
elif choice == "налево" or choice == "2":
    print("\nТы выбрал путь налево.")
    print("Сам погибнешь, но конь твой жив останется.")
elif choice == "прямо" or choice == "3":
    print("\nТы выбрал путь прямо.")
    print("И себя, и коня потеряешь.")
else:
    print("\nТы замешкался на распутье и так и не сделал выбор.")