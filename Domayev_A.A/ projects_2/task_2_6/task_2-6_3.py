print("=== Проверка возможности переливания крови ===\n")

# Ввод данных
donor = input("Введите группу крови донора (A, B, AB, O): ").upper()
recipient = input("Введите группу крови пациента (A, B, AB, O): ").upper()

# Проверка возможности переливания
if donor == recipient or donor == "O":
    print("Переливание возможно.")
else:
    print("Переливание невозможно.")